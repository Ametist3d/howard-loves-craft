const FALLBACK_ORDER: Language[] = ['en', 'ru'];

export const getCharacterFlavor = (language: Language): FlavorSet => {
  return (
    CHARACTER_FLAVOR[language] ??
    CHARACTER_FLAVOR[FALLBACK_ORDER.find((lang) => CHARACTER_FLAVOR[lang]) ?? 'en']
  );
};

export const getRandomOccupation = (language: Language): string => {
  const occupations = getCharacterFlavor(language).occupations;
  return occupations[Math.floor(Math.random() * occupations.length)];
};

export const getRandomBackground = (language: Language): string => {
  const backgrounds = getCharacterFlavor(language).backgrounds;
  return backgrounds[Math.floor(Math.random() * backgrounds.length)];
};

